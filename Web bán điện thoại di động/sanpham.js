//ham addcart
function addcart(code, number) {
    if (number == 0) return; //nếu số lượng sản phẩm = 0 thì không thực hiện gì
    //else 
    if (typeof localStorage[code] === "undefined") { //nếu sản phẩm này chưa có trong danh sach đơn hàng
        window.localStorage.setItem(code, number); // lưu sản phẩm vào storage
    }
    else { //else
        var current = parseInt(window.localStorage.getItem(code)) //current = số lần nhấp vào nó 
        if (current + number - 1 > sanpham[code].soluong) { // số lượng lớn hơn số sản phẩm cong trong kho 
            alert(`Bạn đã mua số lượng lớn số lượng trong kho dã có! Trong kho chỉ còn ${sanpham[code].soluong} sản phẩm.`) //hiện ra dòng lệnh này
        }
        else { //else 
            window.localStorage.setItem(code, parseInt(current) + parseInt(number)); //cập nhật lại số lượng của sản phẩm = current + number
        }

    }//khi nhấn vào thêm vào giỏ hàng 
    alert(`Bạn đã mua sản phẩm ${sanpham[code].name} với số luong ${number}. Sản phẩm của bạn đã được thêm vào đơn hàng thành công.`)

}

//ham showcart
function showcart() {
    var fomatter = new Intl.NumberFormat("vi-VN", { style: "currency", currency: "VND" }) // định dạng tiền ex: 1.000 đ
    var container = document.querySelector(".cart_table").getElementsByTagName("tbody")[0] //lấy thẻ tbody
    container.innerHTML = ""

    var sum = 0; // tổng mỗi mặt hàng
    var total = 0; // tổng số tiền vnd
    var khuyenmai = 0; // tỉ lệ khuyến mãi vnd
    var totalkm = 0; //khuyến mãi của một đơn hàng
    var tongkm = 0; //tổng khuyến mãi của cả đơn hàng
    var tongsl = 0;
    for (var i = 0; i < window.localStorage.length; i++) { // xét danh sách storage
        if (typeof sanpham[localStorage.key(i)] === "undefined") //nếu sản phẩm thứ i chưa có
            continue; // tiếp tục
        //tạo thẻ tr và các thẻ td
        var tr = document.createElement("tr")
        var nameCell = document.createElement("td") // tên sản phẩm
        var photoCell = document.createElement("td")// ảnh
        var numberCell = document.createElement("td")//số lượng
        var priceCell = document.createElement("td")//đơn giá
        var sumCell = document.createElement("td")// thành tiền
        var removeCell = document.createElement("td") //cột xóa
        var removelink = document.createElement("a")//nút xóa sản phẩm


        var item = sanpham[localStorage.key(i)] //lấy sản phẩm i
        var number = localStorage.getItem(localStorage.key(i)) //lấy số lượng sản phẩm thứ i

        nameCell.innerHTML = item.name //in ra màn hình tên sản phẩm

        photoCell.style.textAlign = "center" //css ảnh nằm ở giữa
        photoCell.innerHTML = `<img src="${item.img1}" class="round_figure" alt="">` //int ra màn hình source ảnh

        numberCell.innerHTML = number; //in ra màn hình số lượng sản phẩm
        numberCell.style.textAlign = "center" // css ô số lượng phần tử nằm ở giữa
        khuyenmai = parseInt(item.promotion) //lấy mã khuyến mãi (%)

        priceCell.innerHTML = fomatter.format(parseInt(item.new_price.split(".").join(""))) + `</br><span style="color:red">Vocher khuyến mãi(${khuyenmai}%)</span>` //hiển thị đơn giá và tỷ lệ khuyến mãi
        priceCell.style.textAlign = "right"//css ô đơn giá nằm ở bên phải

        sum = number * parseInt(item.new_price.split(".").join("")) - number * parseInt(item.new_price.split(".").join("")) * khuyenmai / 100 // sum = thành tiền - thành tiền * khuyến mãi /100
        sumCell.innerHTML = fomatter.format(sum) //in ra giá thành tiền
        sumCell.style.textAlign = "right" //css ô thành tienf nằm bên phải

        totalkm = parseInt(fomatter.format(sum).split(".").join("")) * khuyenmai / 100 //khuyến mãi một sản phẩm
        removelink.innerHTML = `<i class="fa-solid fa-xmark"></i>` // hiển thị nút x xóa sản phẩm
        removelink.setAttribute("href", "#") // thêm attribute là href có giá trị là # trang hiện tại
        removelink.setAttribute("data-code", localStorage.key(i)) // lưu mã sản phẩm vào data-code
        removelink.onclick = function () { //click vào dấu x xóa sản phẩm
            removecart(this.dataset.code) // gọi tới hàm removecart truyền vào mã sản phẩm lất từ data-code
        }
        removeCell.style.textAlign = "center" //css ô xóa sản phẩm nằm ở giữa
        removeCell.appendChild(removelink) // thêm removelink vào làm con của removecell

        tr.appendChild(nameCell) //tr thêm con namecell
        tr.appendChild(photoCell)//tr thêm con photocell
        tr.appendChild(numberCell)//tr thêm con numbercell
        tr.appendChild(priceCell)//tr thêm con pricecell
        tr.appendChild(sumCell)//tr thêm con sumcell
        tr.appendChild(removeCell)//tr thêm con removecell
        container.appendChild(tr)//container thêm con tr
        total += sum; // tổng đơn hàng
        tongkm += totalkm; //tổng khuyến mãi
        tongsl += parseInt(number); // tổng số lượng sản phẩm

    }
    window.localStorage.setItem("tongsl", JSON.stringify(tongsl))
    document.querySelector(".sl").innerHTML = tongsl
    var tax = total * 0.1; //thuế
    var tongcong = total + tax - tongkm //tổng thành tiền = tổng đơn hàng + thuế - tổng khuyến mãi
    document.querySelector(".tongtien").innerHTML = fomatter.format(total) // hiển thị tổng đơn hàng
    document.querySelector(".thue").innerHTML = fomatter.format(tax)//hiển thị thuế
    document.querySelector(".tongcong").innerHTML = fomatter.format(tongcong)//hiển thị tổng thành tiền

}
