function kiemtraHTML() {
    var navA = document.querySelectorAll(".nava") //lấy thẻ cần đúc class open vào
    navA.forEach((A, index) => {
        if (A.pathname.split("/").join('') === location.pathname.split("/").join('')) { //nếu pathname == nhau thì 
            var openActive = document.querySelector(".open") // kiểm tra thẻ có open
            if (openActive) openActive.classList.remove("open") //nếu có remove open
            A.classList.add("open"); //add open vào
        }
    })
}

